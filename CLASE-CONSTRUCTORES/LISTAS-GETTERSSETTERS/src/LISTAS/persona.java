package LISTAS;

public class persona {
private String nombre;
private String genero;
private int entero;



public persona(String nombre, String genero, int entero) {
    this.nombre = nombre;
    this.genero = genero;
    this.entero = entero;
}



public String getNombre() {
    return nombre;
}



public String getGenero() {
    return genero;
}



public int getEntero() {
    return entero;
}



}
